% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
global hasil2 hasil1
roc=hasil2+hasil1; 
TN=0;TP=0;FN=0;FP=0;
for i=1:size(roc,1)
    for j=1:size(roc,2) 
        if roc(i,j)==2
            TP=TP+1;
        elseif roc(i,j)==1 
            FN=FN+1;
        end
    end
end
a=sum(sum(hasil2==1)); 
FP=a-TP;
TN=(256*256)-(TP+FN+FP);
[TN FN TP FP]
akurasi_r=100*(TP+TN)/(TP+FN+FP+TN) 
sensitifitas_r=100*(TP/(TP+FN)) 
spesifitas_r=100*(TN/(FP+TN)) 
set(handles.edit7,'String',akurasi_r); 
set(handles.edit8,'String',sensitifitas_r); 
set(handles.edit9,'String',spesifitas_r); 
set(handles.edit3,'String',TN); 
set(handles.edit4,'String',FN);
set(handles.edit5,'String',TP); 
set(handles.edit6,'String',FP);
